package Inheritance;

public class School {
	
	public void scname( String str) {
		System.out.println("the school name is: "+str);
	}
	
	public void scaddress(String st) {
		System.out.println("the school address is: "+st);
	}
	public void stdname(String std) {
		System.out.println("the student name is: "+std);
	}
	public void stdclassname(String n) {
		System.out.println("the school class name is: "+n);
	}
	public void stdaddress(String ad) {
		System.out.println("the student address is: "+ad);
	}

	public void stdmedium(String m) {
		System.out.println("the student medium is: "+m);
	}
}
